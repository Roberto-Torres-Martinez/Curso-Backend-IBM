#Función sum
#Suma de todos los elementos
lista = [1, 2, 3, 4, 5]
resultado = sum(lista)
print(f'Resultado de la suma: {resultado}')

#Proporcionar un valor inicial
resultado = sum(lista, 20)
print(f'Resultado de la suma con valor inicial: {resultado}')

